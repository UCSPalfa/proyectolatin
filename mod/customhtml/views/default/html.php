<h1 class="c5"><a name="h.leueaofct8s9"></a><span class="c12 c7">2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;DESCRIPCI&Oacute;N</span><span class="c12 c7">&nbsp;GENERAL DE LA CONVOCATORIA</span></h1>
<p class="c4">
    <span class="c7"></span>
</p>
<ol class="c10" start="1">
    <li class="c1"><span class="c7">LATIn convoca a grupos de docentes universitarios a presentar propuestas para la creaci&oacute;n de libros de texto abiertos universitarios en las diferentes &aacute;reas del conocimiento, dirigidos a estudiantes de Instituciones de Educaci&oacute;n Superior (IES) de Latinoam&eacute;rica.</span></li>
    <li class="c1"><span class="c7">LATIn financiar&aacute; al menos 16 propuestas de libros de texto abiertos. </span></li>
    <li class="c1"><span class="c7">Las propuestas podr&aacute;n ser presentadas en esta primera oportunidad por grupos de docentes de las IES de Latinoam&eacute;rica que forman parte del consorcio universitario integrante del Proyecto LATIn: Escuela Superior Polit&eacute;cnica del Litoral (ESPOL) - Ecuador, Universidade Presbiteriana Mackenzie (UPM) - Brasil, Universidad de la Rep&uacute;blica (UDELAR) - Uruguay, Universidad Nacional de Rosario (UNR) - Argentina, Universidad Aut&oacute;noma de Aguascalientes (UAA) - M&eacute;xico, Universidad Austral de Chile (UACh) Universidad Central de Venezuela (UCV), Universidad Cat&oacute;lica San Pablo (UCSP) - Per&uacute; y Universidad del Cauca (UNICAUCA) - Colombia.</span></li>
    <li class="c1"><span class="c7">Los grupos deber&aacute;n estar integrados por al menos tres (3) docentes, los cuales deber&aacute;n pertenecer al menos a tres (3) de las IES miembros del consorcio ( mencionadas en el punto c. de esta Secci&oacute;n). Los grupos podr&aacute;n tener una integraci&oacute;n m&aacute;xima de </span><span class="c7">12 docentes</span><span class="c7">&nbsp;por propuesta.</span></li>
    <li class="c1"><span class="c7">Una vez aprobadas y seleccionas las propuestas, los grupos deber&aacute;n dar inicio a la creaci&oacute;n de los libros, que deber&aacute;n ser realizados colaborativamente. LATIn facilitar&aacute; una plataforma tecnol&oacute;gica para favorecer el intercambio de ideas entre colegas, generar una comunidad tem&aacute;tica, propiciar la generaci&oacute;n de grupos de escritura de los libros de texto, desarrollar la escritura colaborativa de los mismos y facilitar su publicaci&oacute;n y distribuci&oacute;n.</span></li>
    <li class="c1"><span class="c7">Los idiomas oficiales para la publicaci&oacute;n son: Espa&ntilde;ol y Portugu&eacute;s.</span></li>
    <li class="c1"><span class="c7">Todos los docentes integrantes del grupo se comprometer&aacute;n a incluir el </span><span class="c7 c31">recuerdo</span><span class="c7">&nbsp;libro de texto creado en la bibliograf&iacute;a de </span><span class="c7">su curso</span><sup><a href="#cmnt1" name="cmnt_ref1">[a]</a></sup><span class="c7">&nbsp;y a utilizar el mismo durante una experiencia piloto a desarrollarse durante el </span><span class="c7">a&ntilde;o 2014.</span></li>
</ol>
