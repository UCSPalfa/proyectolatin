<p class="c4">
    <span class="c7"></span>
</p>
<p class="c26">
    <span class="c7">Etapas sugeridas para la elaboraci&oacute;n: </span>
</p>
<p class="c16">
    <span class="c7"></span>
</p>
<ol class="c10" start="1">
    <li class="c21 c17"><span class="c7">Caracterizaci&oacute;n ajustada de los estudiantes destinatarios y ajuste de los objetivos; </span></li>
    <li class="c17 c21"><span class="c7">Recopilaci&oacute;n de la informaci&oacute;n con base en bibliograf&iacute;a actualizada; </span></li>
    <li class="c21 c17"><span class="c7">Elaboraci&oacute;n de contenidos te&oacute;ricos y pr&aacute;cticos; </span></li>
    <li class="c21 c17"><span class="c7">Elaboraci&oacute;n de actividades, evaluaciones, autoevaluaciones y dem&aacute;s recursos educativos a incorporar; </span></li>
    <li class="c21 c17"><span class="c7">Elaborar bibliograf&iacute;a actualizada, &iacute;ndices, gu&iacute;as de uso; </span></li>
    <li class="c21 c17"><span class="c7">Realizar correcci&oacute;n de estilo y ortograf&iacute;a; </span></li>
    <li class="c21 c17"><span class="c7">Realizar la diagramaci&oacute;n o el dise&ntilde;o gr&aacute;fico en caso de ser necesario; </span></li>
    <li class="c21 c17"><span class="c7">Testeo, Correcci&oacute;n, y versi&oacute;n final.</span></li>
</ol>
<p class="c16">
    <span class="c12 c7"></span>
</p>
<p class="c16">
    <span class="c12 c7"></span>
</p>
<p class="c5">
    <span class="c7">Cada una de estas etapas deber&aacute; contar con la participaci&oacute;n colaborativa de los participantes del grupo.</span>
</p>
<p class="c4">
    <span class="c7"></span>
</p>
<p class="c5">
    <span class="c7">Esta tarea requiere de la definici&oacute;n de acuerdos, roles y tomas de decisiones, as&iacute; como de una estrategia por parte del grupo docente involucrado. Sugerimos algunos posibles modos de organizaci&oacute;n de los procesos de escritura colaborativa:</span>
</p>
<p class="c4">
    <span class="c7"></span>
</p>
<ol class="c10" start="1">
    <li class="c21 c17"><span class="c7">El grupo planea y define la tarea, luego cada participante prepara su parte y el grupo compila las partes individuales y revisa el documento.</span></li>
    <li class="c1"><span class="c7">El grupo planea y define la tarea, luego un integrante del grupo prepara un borrador, el equipo edita y revisa el borrador.</span></li>
    <li class="c1"><span class="c7">Un integrante del grupo planea y escribe un borrador, el grupo revisa el borrador.</span></li>
    <li class="c1"><span class="c7">Una persona planea y escribe el borrador, luego uno o m&aacute;s integrantes del equipo revisa el borrador sin consultar al autor original.</span></li>
    <li class="c1"><span class="c7">El grupo planea y escribe el borrador, uno o m&aacute;s integrantes revisa el borrador sin consultar el autor original.</span></li>
    <li class="c1"><span class="c7">Una persona asigna las tareas, cada integrante completa su trabajo individual, una persona revisa el documento.</span><sup><a href="#cmnt13" name="cmnt_ref13">[m]</a></sup></li>
</ol>
<p class="c4">
    <span class="c7"></span>
</p>
<p class="c5">
    <span class="c7">Tambi&eacute;n es posible que el grupo integre todos o alguno de los siguientes roles:</span>
</p>
<p class="c4">
    <span class="c7"></span>
</p>
<ol class="c10" start="1">
    <li class="c1"><span class="c7">Creador de contenido: este rol representa a una persona o un grupo de personas que utilizan diversas metodolog&iacute;as derivadas de escritura colaborativa para crear contenido sobre un tema espec&iacute;fico.</span></li>
    <li class="c1"><span class="c7">Generador de ideas: este (grupo de) persona (s) identifica el panorama general del proyecto, conduce a los dem&aacute;s hacia el objetivo principal, y mantiene el impulso del proyecto adelante.</span></li>
    <li class="c1"><span class="c7">Colaborador evaluador de contenido: esta funci&oacute;n est&aacute; a cargo de la revisi&oacute;n de los contenidos. Esta persona (o grupo) debe trabajar en estrecha relaci&oacute;n con el Organizador de contenido, proponiendo las modificaciones apropiadas.</span></li>
    <li class="c1"><span class="c7">Revisor t&eacute;cnico: esta persona (o grupo) tiene la funci&oacute;n de control de los aspectos t&eacute;cnicos del libro o el contenido, por ejemplo, comprueba la la consistencia de los enlaces, revisar la edici&oacute;n de f&oacute;rmulas, etc</span></li>
    <li class="c1"><span class="c7">Colaborador pedag&oacute;gico: esta persona se asegura de que la organizaci&oacute;n del contenido tiene sentido desde un punto de vista pedag&oacute;gico. Por lo tanto, &eacute;l / ella trabaja en estrecha colaboraci&oacute;n con el organizador de contenido con el fin de notificar el contenido que falta, o para se&ntilde;alar algunos de los elementos que deben ser m&aacute;s profundizados o incorporados.</span></li>
    <li class="c1"><span class="c7">Evaluador de idioma: un hablante nativo tiene que revisar el libro con el fin para asegurarse de que su ortograf&iacute;a, la sintaxis y la gram&aacute;tica se ajustan a las normas idiom&aacute;ticas.</span></li>
    <li class="c1"><span class="c7">Organizador de contenido: una persona capaz de guiar la creaci&oacute;n de un libro, que tiene ideas claras del plan de estudios y de las necesidades de los estudiantes, y que a la vez proporciona r&aacute;pida revisi&oacute;n y comentarios sobre todos los aspectos del contenido. Adem&aacute;s, &eacute;l / ella se propone la tabla de contenidos y es responsable de definir la estrategia de escritura colaborativa en el grupo y la asignaci&oacute;n de roles en la plataforma.</span></li>
    <li class="c1"><span class="c7">Organizador: esta funci&oacute;n es vital para organizar el equipo que trabaja en el proyecto. La persona a cargo de esta funci&oacute;n debe ser caracterizado por diversas competencias como la gesti&oacute;n de proyectos, buenas relaciones sociales, la experiencia en el &aacute;mbito de la colaboraci&oacute;n, y conocimientos b&aacute;sicos sobre el tema del libro.</span></li>
    <li class="c1"><span class="c7">Dise&ntilde;ador de plantillas de interfaz: este dise&ntilde;ador desarrolla y mantiene la plantilla de cada libro para asegurarse de que el contenido producido por los diferentes colaboradores (Productores de contenidos) que coincida con el mismo </span><span class="c7 c29">look-and-feel</span><span class="c7">. Esta persona es responsable del dise&ntilde;o y la implementaci&oacute;n de las mejoras necesarias en un corto per&iacute;odo de tiempo.</span></li>
    <li class="c1"><span class="c7">Dise&ntilde;ador de la plantilla pedag&oacute;gica: ofrece una manera uniforme para llenar la tabla de contenidos establecidos por el organizador de contenido, y asegura que el contenido de los diversos cap&iacute;tulos se organiza seg&uacute;n una estructura com&uacute;n y siguiendo una estrategia pedag&oacute;gica.</span></li>
    <li class="c1"><span class="c7">Traductor: cuando el idioma nativo de la producci&oacute;n de contenido no coincide con el lenguaje de la audiencia objetivo, el traductor garantiza la traducci&oacute;n de los contenidos a la lengua de destino. A continuaci&oacute;n, la traducci&oacute;n tiene que estar validada por el revisor de idioma. Este papel es crucial para el contexto latinoamericano, en relaci&oacute;n con sus dos principales idiomas: espa&ntilde;ol (alrededor de 360 &#8203;&#8203;millones hablantes nativos en la regi&oacute;n) y portugues (alrededor de 200 millones de hablantes nativos s&oacute;lo en Brasil).</span></li>
</ol>
<p class="c4">
    <span class="c7"></span>
