<h1 class="c5"><a name="h.bk2o6puf4ki"></a><span class="c12 c7">1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ANTECEDENTES Y OBJETIVOS DE LA CONVOCATORIA</span></h1>
<p class="c4">
    <span class="c7"></span>
</p>
<p class="c5">
    <span class="c7">El </span><span class="c20 c7"><a class="c25" href="http://latinproject.org/index.php/es/">Proyecto LATIN</a></span><span class="c7">&nbsp;(Latin American Open Textbook Initiative - Iniciativa Latinoamericana de Libros de Texto Abiertos)</span><sup class="c7"><a href="#ftnt1" name="ftnt_ref1">[1]</a></sup><span class="c7">&nbsp;convoca a grupos de docentes universitarios a presentar propuestas para la creaci&oacute;n de libros de texto abiertos</span><sup class="c7"><a href="#ftnt2" name="ftnt_ref2">[2]</a></sup><span class="c7">&nbsp;universitarios en las diferentes &aacute;reas del conocimiento, dirigidos a estudiantes de Instituciones de Educaci&oacute;n Superior (IES) de Latinoam&eacute;rica.</span>
</p>
<p class="c4">
    <span class="c7"></span>
</p>
<p class="c5">
    <span class="c3">LATIn tiene como objetivo general </span><span class="c7">mejorar el acceso a la Universidad de estudiantes de bajos ingresos en Latinoam&eacute;rica, as&iacute; como a reducir la tasa de deserci&oacute;n escolar debido a razones financieras.</span>
</p>
<p class="c5">
    <span class="c7">El desarrollo de la iniciativa latinoamericana de libros de texto abiertos espera contribuir al logro de este objetivo a trav&eacute;s de la reducci&oacute;n o eliminaci&oacute;n del costo de los libros de texto, los cuales a veces pueden representar una parte significativa de los ingresos de una familia de salario m&iacute;nimo.</span>
</p>
<p class="c4">
    <span class="c7"></span>
</p>
<p class="c5">
    <span class="c7">El objetivo espec&iacute;fico del proyecto es la creaci&oacute;n y difusi&oacute;n de una Iniciativa de Libros de Texto Abiertos para la Educaci&oacute;n Superior, espec&iacute;ficamente desarrollada para el contexto latinoamericano. Esta iniciativa busca fomentar la colaboraci&oacute;n entre profesores y autores acad&eacute;micos de diferentes IES de Latinoam&eacute;rica para crear cap&iacute;tulos y libros de texto adaptados a las necesidades espec&iacute;ficas de sus cursos. El proyecto se ocupar&aacute; de los aspectos pedag&oacute;gicos, tecnol&oacute;gicos y pol&iacute;ticos necesarios para que una iniciativa de este tipo prospere con &eacute;xito en los pa&iacute;ses latinoamericanos.</span>
</p>
<p class="c4">
    <span class="c7"></span>
</p>
<p class="c5">
    <span class="c7">La iniciativa dar&aacute; lugar al desarrollo de una plataforma tecnol&oacute;gica colaborativa para la creaci&oacute;n y publicaci&oacute;n de libros de texto abiertos. Los libros de texto generados a partir de la presente convocatoria, creados espec&iacute;ficamente para la regi&oacute;n, tendr&aacute;n edici&oacute;n gratuita en formato digital y estar&aacute;n publicados en la plataforma del LATIn bajo licencia Creative Commons, permitiendo a estudiantes y profesores, leerlos, imprimirlos, descargarlos, compartirlos, adaptarlos, traducirlos y modificarlos.</span>
</p>
<p class="c4">
