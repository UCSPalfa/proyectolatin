<h2 class="c7"><a name="h.xcea6t4k7fal"></a><span class="c1">8.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;EVALUACI&Oacute;N DE LAS PROPUESTAS </span></h2>
<p class="c12 c10">
